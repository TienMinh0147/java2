    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Book bk = new Book();
        String msa = ms.getText();
        String tsa = ts.getText();
        String tlo = tl.getToolTipText();
        String slu = sl.getText();
        boolean OK = true;

        if (msa.equals("")) {
            OK = false;
            JOptionPane.showMessageDialog(this, "Ban chua nhap ma sach !!!");
        }
        if (tsa.equals("")) {
            OK = false;
            JOptionPane.showMessageDialog(this, "Ban chua nhap ten sach !!!");
        }
        try {
            bk.sl = Integer.parseInt(sl.getText());
            if (sl.equals("")) {
                OK = false;
                JOptionPane.showMessageDialog(this, "Ban chua nhap so luong !!!");
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Ban phai nhap so");
        }
        if (OK) {
            add();
        }

    }//GEN-LAST:event_jButton2ActionPerformed

