    void filltotable() {
        DefaultTableModel Model = (DefaultTableModel) jTable2.getModel();
        Model.setRowCount(0);
        for (Book bk : list) {
            Object[] row = new Object[]{bk.ms, bk.sl, bk.tl, bk.ts
            };
            Model.addRow(row);
        }
    }
    
    void firstlast(int vitri){
        ms.setText(list.get(vitri).getMs());
        ts.setText(list.get(vitri).getTs());
        tl.setToolTipText(list.get(vitri).getTl());
        sl.setText(list.get(vitri).getSl()+"");
    }

    void write() {
        Xfile.writeObject("D:\\Desktop\\TailieuFpoly\\JAV2\\Lab\\JavaApplication11\\baithi.txt", list);
    }

    void remove() {
        int vitri = jTable2.getSelectedRow();
        list.remove(vitri);
        filltotable();
        JOptionPane.showMessageDialog(this, "ĐÃ XÓA THÀNH CÔNG !!!");
    }

    void add() {
        String msa = ms.getText();
        String tsa = ts.getText();
        String tlo = tl.getSelectedItem().toString();
        String slu = sl.getText();
        Book obj = new Book(msa, tsa, tlo, Integer.parseInt(slu));
        list.add(obj);
        JOptionPane.showMessageDialog(this, "Them Thanh Cong");
        filltotable();
    }

